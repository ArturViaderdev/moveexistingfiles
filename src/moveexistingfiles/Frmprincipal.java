    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        File folder = new File(tdir.getText());
        File[] listOfFiles = folder.listFiles();

        for (int i = 0; i < listOfFiles.length; i++) {
            if (listOfFiles[i].isFile()) {
                File f = new File(tsacar.getText() + "/" + listOfFiles[i].getName());
                if (f.exists() && !f.isDirectory()) {

                    if (f.renameTo(new File(tdestino.getText() + "/" + listOfFiles[i].getName()))) {
                        System.out.println("Rom movida");
                    } else {
                        System.out.println("Error al mover rom.");
                    }
                }

                System.out.println("File " + listOfFiles[i].getName());
            }
        }
        System.out.println("Proceso completado.");
    }//GEN-LAST:event_jButton4ActionPerformed

